package com.nikesh.springboot.java.telusko;

// final - variable, method, class
// Final variables - One any variable is declared as final No one can change the value.
// Final method - If we make a method as final, No one can override the method.
// Final class - If a class is declared as final, No other class can extend this class.

public class FinalKeyword {
    public static void main(String[] args) {
        final double PI = 3.14;
    }
}
